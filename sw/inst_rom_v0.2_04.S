	.org 0x0
.global _start
   .set noat
_start:
   lui  $2,0x0404			# $2 						= 0x04040000
   ori  $2,$2,0x0404		# $2 = 0x04040000 | 0x0404	= 0x04040404
   ori	$7,$0,0x7			# $7 = 0x00000000 | 0x7		= 0x7
   ori	$5,$0,0x5			# $5 = 0x00000000 | 0x5		= 0x5
   ori	$8,$0,0x8			# $8 = 0x00000000 | 0x8		= 0x8
   sync
   sll	$2,$2,8				# $2 = 0x04040404 << 8		= 0x04040400
   sllv	$2,$2,$7			# $2 = 0x04040400 << 7		= 0x02020000
   srl	$2,$2,8				# $2 = 0x02020000 >> 8		= 0x00020200
   srlv	$2,$2,$5			# $2 = 0x00020200 >> 5		= 0x00001010
   